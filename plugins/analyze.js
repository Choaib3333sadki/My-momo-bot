import axios from 'axios'
import crypto from 'crypto'
import FormData from 'form-data'
import fs from 'fs'

function detectLanguage(text = '') {
    // Simple Arabic detection
    const arabicRegex = /[\u0600-\u06FF]/
    if (arabicRegex.test(text)) return 'ar'
    return 'en'
}

let handler = async (m, { conn }) => {
    try {
        if (!m.quoted || !/image/.test(m.quoted.mimetype)) {
            return m.reply(
                `üìå *Image Analyzer Feature*\n\n` +
                `Reply to an image using:\n.analyze your question`
            )
        }

        const media = await m.quoted.download()
        const filePath = `./temp_${Date.now()}.jpg`
        fs.writeFileSync(filePath, media)

        const userPrompt = m.text.replace('.analyze', '').trim() || "What is this image?"
        const detectedLang = detectLanguage(userPrompt)

        await m.reply("‚è≥ Uploading image...")

        // Upload image
        const formData = new FormData()
        formData.append('reqtype', 'fileupload')
        formData.append('fileToUpload', fs.createReadStream(filePath))

        const uploadResponse = await axios.post(
            'https://catbox.moe/user/api.php',
            formData,
            { headers: formData.getHeaders() }
        )

        const imageUrl = uploadResponse.data.trim()
        const conversationId = crypto.randomUUID()

        await m.reply("ü§ñ Analyzing image...")

        const payload = {
            message: userPrompt,
            language: detectedLang, // üî• now automatic
            model: "gemini-3-flash-preview",
            tone: "default",
            length: "moderate",
            conversation_id: conversationId,
            image_urls: [imageUrl],
            stream_url: "/api/v2/homework/stream"
        }

        const response = await axios.post(
            'https://notegpt.io/api/v2/homework/stream',
            payload,
            {
                headers: {
                    'Content-Type': 'application/json',
                    'Origin': 'https://notegpt.io',
                    'Referer': 'https://notegpt.io/ai-answer-generator',
                    'User-Agent': 'Mozilla/5.0'
                },
                responseType: 'stream'
            }
        )

        let fullText = ''

        await new Promise((resolve, reject) => {
            response.data.on('data', (chunk) => {
                const lines = chunk.toString().split('\n')
                for (const line of lines) {
                    if (line.startsWith('data: ')) {
                        try {
                            const jsonStr = line.slice(6)
                            if (!jsonStr) continue
                            const data = JSON.parse(jsonStr)
                            if (data.text) fullText += data.text
                            if (data.done) resolve()
                        } catch {}
                    }
                }
            })

            response.data.on('end', resolve)
            response.data.on('error', reject)
        })

        fs.unlinkSync(filePath)

        if (!fullText) return m.reply("‚ùå No response received.")

        await m.reply(fullText)

    } catch (err) {
        console.error(err)
        m.reply("‚ùå Error while processing the image.")
    }
}

handler.help = ['analyze']
handler.command = ['analyze']
handler.tags = ['ai','morocco']
handler.limit = true

export default handler
